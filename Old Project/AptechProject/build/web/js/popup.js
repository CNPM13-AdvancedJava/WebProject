function popup(){
	var x1 = document.getElementById('light');
	var x2 = document.getElementById('fade');
	x1.style.display='block';
	x2.style.display='block'
}
function endpopup(){
	var x1 = document.getElementById('light')
	x1.style.display='none';
	var x2 = document.getElementById('fade')
	x2.style.display='none'
}


